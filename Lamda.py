def safe_p(p, default=350.0):
    """Safely parse price from number or string."""
    try:
        if isinstance(p, (int, float)):
            return abs(float(p))
        if isinstance(p, str):
            import re
            nums = re.findall(r'\d+', p)
            if nums:
                return float(nums[0])
    except:
        pass
    return float(default)

