def scan_passport_with_vision(image_base64, mime_type="image/jpeg"):
    """
    Use Claude's vision capability to read a passport image and extract
    structured fields from the biographical data page / MRZ zone.
    Returns a dict with firstName, lastName, passportNumber, nationality,
    dob, gender, passportExpiry, issuingCountry — or raises on failure.
    """
    system_prompt = """
You are a passport OCR specialist. The user has uploaded a photo of a passport biographical data page.
Extract ALL readable fields from the MRZ (machine-readable zone) and the visual data page.

Return ONLY valid JSON in exactly this format — no markdown, no explanation:
{
  "firstName": "string",
  "lastName": "string",
  "passportNumber": "string",
  "nationality": "string (full country name e.g. Ethiopian, not code)",
  "dob": "YYYY-MM-DD",
  "gender": "Male or Female",
  "passportExpiry": "YYYY-MM-DD",
  "issuingCountry": "string (full country name)"
}

RULES:
- Read the MRZ lines at the bottom of the page for reliable data.
- MRZ date format is YYMMDD — convert to YYYY-MM-DD (assume 19xx if YY >= 30, 20xx if YY < 30).
- If a field is illegible or missing, use an empty string "".
- Do NOT invent or guess values; only extract what is clearly visible.
- Do NOT include any extra keys.
"""

    try:
        resp = bedrock.converse(
            modelId=INFERENCE_PROFILE_ARN,
            messages=[{
                "role": "user",
                "content": [
                    {
                        "image": {
                            "format": mime_type.split("/")[-1].replace("jpeg", "jpeg").replace("jpg", "jpeg"),
                            "source": {
                                "bytes": base64.b64decode(image_base64)
                            }
                        }
                    },
                    {
                        "text": "Please extract all passport fields from this image and return the JSON."
                    }
                ]
            }],
            system=[{"text": system_prompt}],
            inferenceConfig={"temperature": 0.0, "maxTokens": 500}
        )

        raw = resp["output"]["message"]["content"][0]["text"].strip()
        # Strip markdown code fences if present
        if "```" in raw:
            raw = raw.split("```")[1].split("```")[0].strip()
            if raw.startswith("json"):
                raw = raw[4:].strip()

        start = raw.find("{")
        end   = raw.rfind("}") + 1
        if start != -1 and end > start:
            raw = raw[start:end]

        data = json.loads(raw)
        print(f"[PassportOCR] Extracted: {data}")
        return data

    except Exception as e:
        print(f"[PassportOCR] Error: {e}")
        raise ValueError(f"Could not read passport: {e}")


