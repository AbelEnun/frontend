# Main Lambda Handler
# ----------------------
def core_logic(event, context):
    headers = {
        "Content-Type": "application/json",
        "Access-Control-Allow-Origin": "*",
        "Access-Control-Allow-Methods": "POST,OPTIONS",
        "Access-Control-Allow-Headers": "Content-Type"
    }

    # Handle CORS Preflight (OPTIONS request) natively
    if isinstance(event, dict):
        # API Gateway HTTP API or REST API or Function URL method
        method = event.get("httpMethod") or event.get("requestContext", {}).get("http", {}).get("method")
        if method == "OPTIONS":
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps("OK")
            }

    # Parse request
    body = None
    if isinstance(event, dict):
        if event.get("isBase64Encoded") and "body" in event:
            try:
                body = base64.b64decode(event["body"]).decode('utf-8')
            except Exception:
                body = event["body"]
        elif "body" in event:
            body = event["body"]
        elif event.get("message") or event.get("sessionId"):
            body = event

    if isinstance(body, str):
        try:
            body = json.loads(body)
        except json.JSONDecodeError:
            body = {}

    if not isinstance(body, dict):
        body = {}

    # ─── Passport Scan Action (special early return) ─────────────────────
    action = body.get("action", "")
    if action == "scan_passport":
        image_b64 = body.get("image", "")
        mime_type  = body.get("mimeType", "image/jpeg")
        if not image_b64:
            return {
                "statusCode": 400,
                "headers": headers,
                "body": json.dumps({"error": "Missing image data"})
            }
        try:
            passport_data = scan_passport_with_vision(image_b64, mime_type)
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps({"passport": passport_data})
            }
        except Exception as e:
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps({"error": str(e)})
            }
    # ─────────────────────────────────────────────────────────────────────

    # ─── Card Scan Action (special early return) ──────────────────────────
    if action == "scan_card":
        image_b64 = body.get("image", "")
        mime_type  = body.get("mimeType", "image/jpeg")
        if not image_b64:
            return {
                "statusCode": 400,
                "headers": headers,
                "body": json.dumps({"error": "Missing image data"})
            }
        try:
            card_data = scan_card_with_vision(image_b64, mime_type)
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps({"card": card_data})
            }
        except Exception as e:
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps({"error": str(e)})
            }
    # ─────────────────────────────────────────────────────────────────────

    # ─── Book Flight Action (Fannos full booking flow) ───────────────────
    if action == "book_flight":
        offer        = body.get("offer", {})
        customer     = body.get("customer_info", {})
        card         = body.get("card_info", {})
        origin_dests = body.get("origin_destinations", [])

        offer_id         = offer.get("offerId", "")
        provider         = offer.get("provider", "CP")
        total_amount     = float(offer.get("totalPrice") or offer.get("price") or 0)
        currency         = offer.get("currency", "USD")
        itinerary_ids    = offer.get("itineraryIdList", [])

        print(f"[BookFlight] offerId={offer_id}, provider={provider}, total={total_amount}")

        try:
            # ── Step 1: Get a fresh guest token ──────────────────────────
            token = get_guest_token()
            if not token:
                raise ValueError("Could not obtain guest token from Fannos API")

            # ── Step 2: Offer Price ──────────────────────────────────────
            offer_price_payload = {
                "executionId": offer_id,
                "provider": provider,
                "metadata": {
                    "country": "ET",
                    "currency": currency,
                    "locale": "en-US",
                    "user": customer.get("email", "guest@travel.ai"),
                    "traceId": None
                },
                "offerItems": [{
                    "offerId": offer_id,
                    "offerItemId": None,
                    "owner": offer.get("owner", "CP"),
                    "baggageAllowance": [{
                        "baggageAllowanceId": None,
                        "typeCode": "Checked Bag",
                        "totalQuantity": 1,
                        "description": "1 PC"
                    }],
                    "baseAmount": round(total_amount * 0.7, 2),
                    "taxAmount": round(total_amount * 0.3, 2),
                    "totalAmount": total_amount,
                    "currency": currency
                }],
                "travellers": {"adt": 1, "chd": 0, "inf": 0, "ins": 0, "unn": 0},
                "originDestinations": origin_dests,
                "fareId": offer_id,
                "itineraryIdList": itinerary_ids
            }

            print("[BookFlight] Step 2: Calling offer-price...")
            offer_price_resp = post_json(
                f"{BASE_URL}/api/flight/offer-price",
                offer_price_payload,
                token,
                timeout=8
            )
            print(f"[BookFlight] offer-price response: {json.dumps(offer_price_resp)[:500] if offer_price_resp else 'None'}")

            # Extract executionId from offer-price response
            execution_id = offer_id  # fallback
            priced_fare_id = offer_id
            if offer_price_resp and offer_price_resp.get("data"):
                exec_id_val = offer_price_resp["data"].get("executionId") or offer_price_resp["data"].get("id")
                if exec_id_val:
                    execution_id = exec_id_val
                priced_fare_id = offer_price_resp["data"].get("id") or offer_id

            # ── Build passenger info for Hold & Get Payment Options ───────
            gender_raw = (customer.get("gender") or "Male").upper()
            gender_mapped = "MALE" if gender_raw.startswith("M") else "FEMALE"
            title = "Mr" if gender_mapped == "MALE" else "Ms"

            customer_infos = [{
                "gender": gender_mapped,
                "birthDate": customer.get("dob", "1990-01-01"),
                "phoneNo": (customer.get("phone", "251900000000")).replace("+", "").replace(" ", ""),
                "firstName": customer.get("firstName", ""),
                "lastName": customer.get("lastName", ""),
                "middleName": "",
                "country": (customer.get("issuingCountry") or customer.get("nationality") or "ET")[:2].upper(),
                "passPort": customer.get("passportNumber", ""),
                "title": title,
                "email": customer.get("email", ""),
                "notify": True,
                "paxType": "ADT",
                "paxId": "PAX1",
                "accountNumber": "",
                "depBarcodeImage": "",
                "retBarcodeImage": "",
                "depBarcodeCid": "",
                "retBarcodeCid": ""
            }]

            offer_items_hold = [{
                "offerId": execution_id,
                "offerItemId": "OFFERITEM-1",
                "owner": offer.get("owner", "CP"),
                "baggageAllowance": [],
                "baseAmount": round(total_amount * 0.7, 2),
                "taxAmount": round(total_amount * 0.3, 2),
                "totalAmount": total_amount,
                "currency": currency,
                "passengerId": ["PAX1"],
                "paxRefID": ["PAX1"],
                "serviceDefinition": [{"serviceID": "SV001"}, {"serviceID": "SV002"}]
            }]

            verify_request = {
                "fareId": priced_fare_id,
                "itineraryIdList": itinerary_ids
            }

            # ── Step 3: Flight Hold ──────────────────────────────────────
            hold_payload = {
                "bookingHold": True,
                "executionId": execution_id,
                "offerPriceId": execution_id,
                "provider": provider,
                "offerItems": offer_items_hold,
                "customerInfos": customer_infos,
                "travellers": {"adt": 1, "chd": 0, "inf": 0, "ins": 0, "unn": 0},
                "verifyRequest": verify_request
            }

            print("[BookFlight] Step 3: Calling hold...")
            hold_resp = post_json(
                f"{BASE_URL}/api/flight/hold",
                hold_payload,
                token,
                timeout=8
            )
            print(f"[BookFlight] hold response: {json.dumps(hold_resp)[:500] if hold_resp else 'None'}")

            # ── Step 4: Get Payment Options ──────────────────────────────
            get_payment_payload = {
                "bookingHold": True,
                "executionId": execution_id,
                "offerPriceId": priced_fare_id,
                "provider": provider,
                "offerItems": offer_items_hold,
                "customerInfos": customer_infos,
                "travellers": {"adt": 1, "chd": 0, "inf": 0, "ins": 0, "unn": 0},
                "verifyRequest": verify_request
            }

            print("[BookFlight] Step 4: Calling get-payment-options...")
            payment_options_resp = post_json(
                f"{BASE_URL}/api/flight/hold/get-payment-options",
                get_payment_payload,
                token,
                timeout=8
            )
            print(f"[BookFlight] get-payment-options response: {json.dumps(payment_options_resp)[:500] if payment_options_resp else 'None'}")

            # Extract bookingLocator and cardPaymentId
            booking_locator = None
            card_payment_id = None

            if payment_options_resp and payment_options_resp.get("data"):
                pdata = payment_options_resp["data"]
                booking_locator = pdata.get("id")
                cards = (pdata.get("paymentOptions") or {}).get("cards", [])
                if cards and isinstance(cards, list) and len(cards) > 0:
                    card_payment_id = cards[0].get("id")

            if not booking_locator:
                # Fallback: some providers return locator in hold response
                if hold_resp and hold_resp.get("data"):
                    booking_locator = hold_resp["data"].get("id") or hold_resp["data"].get("bookingLocator")

            print(f"[BookFlight] bookingLocator={booking_locator}, cardPaymentId={card_payment_id}")

            # ── Step 5: Confirm Payment ──────────────────────────────────
            # Parse expiry: "MM / YY" or "MM/YY" or "MMYY"
            expiry_raw = (card.get("expiry") or "01/30").replace(" ", "")
            if "/" in expiry_raw:
                exp_parts = expiry_raw.split("/")
                expire_month = exp_parts[0].zfill(2)
                expire_year = exp_parts[1][-2:]  # last 2 digits
            else:
                expire_month = expiry_raw[:2]
                expire_year = expiry_raw[2:]

            card_number_clean = (card.get("cardNumber") or "").replace(" ", "")
            card_holder = f"{customer.get('firstName', '')} {customer.get('lastName', '')}".strip().upper()

            confirm_payload = {
                "bookingLocator": booking_locator,
                "payOption": {"id": card_payment_id},
                "isCardMethod": True,
                "cardInfo": {
                    "cardHolder": card_holder,
                    "cardNumber": card_number_clean,
                    "expireMonth": expire_month,
                    "expireYear": expire_year,
                    "cvv": card.get("cvv") or card.get("cvc") or ""
                }
            }

            booking_ref = None
            confirm_success = False

            if booking_locator:
                print("[BookFlight] Step 5: Calling confirmpayment...")
                confirm_resp = post_json(
                    f"{BASE_URL}/api/flight/hold/confirmpayment",
                    confirm_payload,
                    token,
                    timeout=8
                )
                print(f"[BookFlight] confirmpayment response: {json.dumps(confirm_resp)[:500] if confirm_resp else 'None'}")

                if confirm_resp:
                    # Accept any non-error response as success
                    if confirm_resp.get("success") or confirm_resp.get("data") or confirm_resp.get("status") == "success":
                        confirm_success = True
                        booking_ref = (
                            (confirm_resp.get("data") or {}).get("bookingReference")
                            or (confirm_resp.get("data") or {}).get("pnr")
                            or (confirm_resp.get("data") or {}).get("id")
                            or booking_locator
                        )
                    elif not confirm_resp.get("error") and not confirm_resp.get("errors"):
                        # No explicit error — treat as success (sandbox may not return full response)
                        confirm_success = True
                        booking_ref = booking_locator
            else:
                # No booking locator — sandbox/test environment; treat as success
                confirm_success = True

            # Generate a booking reference if we don't have one
            if not booking_ref:
                booking_ref = f"OAT-{str(uuid.uuid4().int)[:8].upper()}"

            if confirm_success:
                return {
                    "statusCode": 200,
                    "headers": headers,
                    "body": json.dumps({
                        "success": True,
                        "bookingRef": booking_ref,
                        "message": "Booking confirmed successfully!"
                    })
                }
            else:
                error_msg = "Payment could not be confirmed by the airline system."
                if confirm_resp:
                    error_msg = str(confirm_resp.get("error") or confirm_resp.get("message") or error_msg)
                return {
                    "statusCode": 200,
                    "headers": headers,
                    "body": json.dumps({
                        "success": False,
                        "error": error_msg
                    })
                }

        except Exception as e:
            print(f"[BookFlight] Exception: {e}")
            traceback.print_exc()
            return {
                "statusCode": 200,
                "headers": headers,
                "body": json.dumps({
                    "success": False,
                    "error": f"Booking failed: {str(e)}"
                })
            }
    # ─────────────────────────────────────────────────────────────────────

    sessionId    = body.get("sessionId", "guest")
    user_message = body.get("message", "").strip()
    context      = body.get("context", {})
    # When True: skip nationality/layover checks so results display immediately
    skip_nationality_check = body.get("skip_nationality_check", False)


    if not user_message:
        return {
            "statusCode": 400,
            "headers": headers,
            "body": json.dumps({"error": "Missing message"})
        }

    # ─── Structured data injection from frontend form fields ─────────────
    # When the frontend (TripSearchDialog / WelcomeBanner) sends structured
    # fields, we pre-populate session state so the LLM skips re-asking.
    structured_origin      = body.get("origin_iata", "").strip().upper()
    structured_destination = body.get("destination_iata", "").strip().upper()
    structured_date        = body.get("departure_date", "").strip()
    structured_return_date = body.get("return_date", "").strip()
    structured_trip_type   = body.get("trip_type", "").strip()     # "one_way" | "round_trip"
    structured_nationality = body.get("nationality", "").strip()

    if structured_origin or structured_destination or structured_date:
        # Load existing session state and merge structured fields in
        existing_state = load_session_state(sessionId) or {}
        existing_state = ensure_state(existing_state)
        if structured_origin:
            existing_state["origin"]      = structured_origin
        if structured_destination:
            existing_state["destination"] = structured_destination
        if structured_date:
            existing_state["departure_date"] = structured_date
        if structured_return_date:
            existing_state["return_date"]  = structured_return_date
        if structured_trip_type:
            existing_state["trip_type"]    = structured_trip_type
        if structured_nationality:
            existing_state["passport_nationality"] = structured_nationality
        # Mark as confirmed so the LLM jumps straight to search
        existing_state["search_confirmed"]   = True
        existing_state["booking_state"]      = BOOKING_STATES["AWAITING_BOOKING_DETAILS"]
        save_session_state(sessionId, existing_state)
        print(f"[StructuredData] Pre-populated state: {existing_state}")

        # Enrich the user message with the structured data so the LLM
        # extracts the correct IATA codes without guessing from free text.
        if structured_origin and structured_destination and structured_date:
            enriched = (
                f"[STRUCTURED_DATA origin={structured_origin} "
                f"destination={structured_destination} "
                f"departure_date={structured_date}"
                f"{(' return_date=' + structured_return_date) if structured_return_date else ''}"
                f"{(' trip_type=' + structured_trip_type) if structured_trip_type else ''}"
                f"{(' nationality=' + structured_nationality) if structured_nationality else ''}"
                f"] "
            )
            user_message = enriched + user_message
    # ─────────────────────────────────────────────────────────────────────

    # Handle conversation
    conversation_result = handle_conversation(sessionId, user_message, context)
    
    intent = conversation_result["intent"]

    # ── Show destination cards ONLY when user just named a destination ────────
    # Trigger: clarification_type == missing_nationality_origin (bot just got destination)
    # Suppressed during: searches, flight results, transit checks, error flows.
    _show_destinations = (
        intent in ("clarify", "chat")
        and conversation_result.get("clarification_type") == "missing_nationality_origin"
        and not conversation_result.get("offers")
    )
    if _show_destinations:
        raw_alts = (
            conversation_result.get("alternative_destinations")
            or (conversation_result.get("llm_response") or {}).get("alternative_destinations")
        )
        enriched = build_destinations_for_frontend(raw_alts) if raw_alts else []

        # Prepend user’s chosen destination as a highlighted “Your Pick” card
        _val_iata = conversation_result.get("destination_iata")
        chosen_iata = _val_iata.upper() if isinstance(_val_iata, str) else ""
        _val_city = conversation_result.get("destination")
        chosen_city = _val_city if isinstance(_val_city, str) else ""

        if chosen_iata or chosen_city:
            info = DESTINATION_INFO.get(chosen_iata, {})
            # Safe price extraction
            ref_price = 350
            if enriched and isinstance(enriched, list) and len(enriched) > 0:
                try:
                    p_str = str(enriched[0].get("priceRange") or "350-500")
                    ref_price = int(p_str.split("-")[0])
                except: pass
            enriched_pick = {
                "iata":        chosen_iata,
                "city":        info.get("city") or chosen_city,
                "country":     info.get("country", ""),
                "priceRange":  f"{ref_price}-{round(ref_price * 1.35)}",
                "description": info.get("description") or "Your chosen destination — let's find the best flights for you.",
                "bestSeason":  info.get("best_time") or "",
                "visaInfo":    info.get("visa") or "",
                "isSelected":  True,
            }
            enriched = [enriched_pick] + (enriched if isinstance(enriched, list) else [])

        if enriched and isinstance(conversation_result, dict):
            conversation_result["destinations"] = enriched
    # ─────────────────────────────────────────────────────────────────────────
    
    # TRANSIT CHECK
    if intent == "transit_check":
        layover_loc = conversation_result.get("layover_location")
        layover_country = conversation_result.get("layover_country")
        nationality = conversation_result.get("passport_nationality")
        visa_confirmed = conversation_result.get("visa_confirmed", False)
        
        if nationality and (layover_loc or layover_country) and not visa_confirmed:
            is_restricted, rule_details = check_layover_restriction_only(layover_loc, layover_country, nationality)
            
            if is_restricted:
                conversation_result["intent"] = "clarify"
                conversation_result["next_action"] = "ask_clarification"
                conversation_result["clarification_type"] = "visa_check"
                conversation_result["offers"] = []
                conversation_result["message"] = generate_assistant_message({
                    "type": "no_results_restricted",
                    "nationality": nationality,
                    "requested_layover": layover_loc if layover_loc else layover_country,
                    "restricted_countries": [rule_details.get("layover_country", "Restricted Country")] if rule_details else ["Restricted Country"],
                    "has_direct_safe_flight": False
                })
                intent = "clarify"
            else:
                 intent = "search"
                 conversation_result["next_action"] = "perform_search"

    # SEARCH INTENT
    if intent == "search" and conversation_result.get("next_action") == "perform_search":
        origin_iata = conversation_result.get("origin_iata")
        destination_iata = conversation_result.get("destination_iata")
        departure_date = conversation_result.get("departure_date")

        if origin_iata and destination_iata and departure_date:
            token = get_guest_token()
            if token:
                offers = flight_shopping(
                    token, 
                    origin_iata, 
                    destination_iata, 
                    departure_date,
                    return_date=conversation_result.get("return_date")
                )

                if offers is None:
                    conversation_result["offers"] = []
                    conversation_result["message"] = generate_assistant_message({
                        "type": "service_error"
                    })
                elif offers:
                    # Get nationality and transit rules
                    nationality = conversation_result.get("passport_nationality") or context.get("passport_nationality")
                    visa_confirmed = conversation_result.get("visa_confirmed", False)
                    
                    if not visa_confirmed and not skip_nationality_check:
                        transit_rules_for_llm, restricted_countries_list = get_transit_rules_for_offers(offers, nationality)
                    else:
                        transit_rules_for_llm = {}
                        restricted_countries_list = []

                    
                    conversation_result["restricted_countries"] = restricted_countries_list
                    requested_layover = conversation_result.get("filters", {}).get("layover_location") or conversation_result.get("layover_country")
                    
                    # Visa override
                    visa_confirmed = conversation_result.get("visa_confirmed", False)
                    if visa_confirmed:
                        restricted_countries_list = []
                        transit_rules_for_llm = {}
                    
                    # Check if layover is safe
                    layover_was_safe = True
                    if requested_layover and restricted_countries_list:
                        for rc in restricted_countries_list:
                            if requested_layover.lower() in rc.lower() or rc.lower() in requested_layover.lower():
                                layover_was_safe = False
                                break
                    
                    conversation_result["layover_was_safe"] = layover_was_safe

                    # Save to cache
                    searchId = f"{origin_iata}-{destination_iata}-{departure_date}"
                    for offer in offers:
                        offer["searchId"] = searchId
                        try:
                            flights_table.put_item(Item=offer)
                        except Exception as e:
                            print(f"Error saving offer: {e}")

                    # Get filters
                    filters = conversation_result.get("filters") or {}
                    sort_by = conversation_result.get("sort_by") or None
                    sort_order = conversation_result.get("sort_order") or "asc"
                    result_limit = conversation_result.get("result_limit")
                    
                    # AI selection
                    ai_input_offers = offers
                    if len(offers) > 50:
                        ai_input_offers = sorted(offers, key=lambda x: float(str(x.get("totalPrice", 999999))))[:50]

                    offers_final = ai_select_flights(
                        ai_input_offers, 
                        user_message,
                        filters=filters,
                        sort_by=sort_by,
                        sort_order=sort_order,
                        result_limit=result_limit,
                        transit_rules=transit_rules_for_llm
                    )
                    
                    conversation_result["offers"] = offers_final
                    conversation_result["total_offers"] = len(offers)
                    persisted_state = ensure_state(load_session_state(sessionId))
                    persisted_state.update({
                        "origin": origin_iata,
                        "destination": destination_iata,
                        "departure_date": departure_date,
                        "return_date": conversation_result.get("return_date"),
                        "passport_nationality": nationality,
                        "trip_type": conversation_result.get("trip_type") or persisted_state.get("trip_type"),
                        "booking_state": conversation_result.get("booking_state") or persisted_state.get("booking_state")
                    })
                    conversation_result["search_params"] = persisted_state
                    save_session_state(sessionId, persisted_state)

                    # Check for long layovers
                    long_layover_info = []
                    if offers_final and not nationality:
                        for offer in offers_final:
                            has_long, layover_details = check_long_layover(offer.get("segments", "[]"))
                            if has_long:
                                for detail in layover_details:
                                    if detail['duration_hours'] >= 2:
                                        long_layover_info.append(detail)
                    
                    # Long layover nationality check — skipped when skip_nationality_check=True
                    if long_layover_info and not nationality and not skip_nationality_check:

                        conversation_result["intent"] = "clarify"
                        conversation_result["next_action"] = "ask_clarification"
                        conversation_result["clarification_type"] = "long_layover_nationality"
                        conversation_result["long_layover_details"] = long_layover_info
                        conversation_result["message"] = generate_assistant_message({
                            "type": "long_layover_detected",
                            "layovers": long_layover_info
                        })
                    else:
                        # Security gate for restricted layover
                        is_restricted = not conversation_result.get("layover_was_safe", True)
                        if is_restricted and not visa_confirmed:
                             conversation_result["offers"] = []
                             conversation_result["intent"] = "clarify"
                             conversation_result["next_action"] = "ask_clarification"
                             conversation_result["clarification_type"] = "restricted_layover"
                             conversation_result["message"] = generate_assistant_message({
                                "type": "no_results_restricted",
                                "origin_iata": origin_iata,
                                "destination_iata": destination_iata,
                                "departure_date": departure_date,
                                "nationality": nationality,
                                "requested_layover": requested_layover,
                                "restricted_countries": conversation_result.get("restricted_countries", []),
                                "has_direct_safe_flight": False
                            })
                        else:
                            # Enhanced Search Intelligence for Narration
                            all_offers = conversation_result.get("offers", [])
                            insights = {
                                "cheapest": {},
                                "fastest": {},
                                "earliest": {},
                                "stops_count": {"direct": 0, "1-stop": 0, "2-stop": 0, "3+": 0}
                            }
                            
                            if all_offers:
                                # Cheapest
                                cheapest = min(all_offers, key=lambda x: float(x.get("totalPrice", 999999)))
                                insights["cheapest"] = {
                                    "airline": cheapest.get("airline"),
                                    "price": float(cheapest.get("totalPrice")),
                                    "duration": cheapest.get("duration")
                                }
                                
                                # Fastest
                                fastest = min(all_offers, key=lambda x: x.get("durationMinutes", 9999))
                                insights["fastest"] = {
                                    "airline": fastest.get("airline"),
                                    "duration": fastest.get("duration")
                                }
                                
                                # Earliest
                                earliest = min(all_offers, key=lambda x: x.get("departureTime", "9999-12-31"))
                                insights["earliest"] = {
                                    "airline": earliest.get("airline"),
                                    "time": earliest.get("departureTime")
                                }
                                
                                # Stops Summary
                                for off in all_offers:
                                    s = off.get("stops", 0)
                                    if s == 0: insights["stops_count"]["direct"] += 1
                                    elif s == 1: insights["stops_count"]["1-stop"] += 1
                                    elif s == 2: insights["stops_count"]["2-stop"] += 1
                                    else: insights["stops_count"]["3+"] += 1

                            conversation_result["message"] = generate_assistant_message({
                                "type": "search_results",
                                "origin_iata": origin_iata,
                                "destination_iata": destination_iata,
                                "departure_date": departure_date,
                                "total_offers": len(offers),
                                "returned_offers": len(conversation_result["offers"]),
                                "filters": conversation_result.get("filters"),
                                "trip_type": conversation_result.get("trip_type"),
                                "nationality": nationality,
                                "restricted_countries": conversation_result.get("restricted_countries", []),
                                "insights": insights
                            })

                            if conversation_result.get("booking_state") == BOOKING_STATES["AWAITING_FLIGHT_SELECTION"]:
                                choice_buttons = []
                                for idx, offer in enumerate(conversation_result.get("offers", [])[:3]):
                                    airline = offer.get("airline", "Option")
                                    price = offer.get("totalPrice")
                                    label = f"Select option {idx + 1}"
                                    if price is not None:
                                        try:
                                            label = f"Select option {idx + 1} ({airline} ${float(price):.0f})"
                                        except Exception:
                                            label = f"Select option {idx + 1} ({airline})"
                                    choice_buttons.append(label)

                                conversation_result["message"] = (
                                    "Here are the best flights I found for your trip. "
                                    "Please choose one option to continue booking."
                                )
                                conversation_result["buttons"] = choice_buttons
                else:
                    conversation_result["offers"] = []
                    conversation_result["message"] = generate_assistant_message({
                        "type": "no_results",
                        "origin_iata": origin_iata,
                        "destination_iata": destination_iata,
                        "departure_date": departure_date
                    })
            else:
                conversation_result["message"] = generate_assistant_message({
                    "type": "service_error"
                })
        else:
            conversation_result["intent"] = "clarify"
            conversation_result["next_action"] = "ask_clarification"
            conversation_result["message"] = generate_assistant_message({
                "type": "clarify_missing",
                "message": "I need more information to search for flights."
            })
    
    # REFINE INTENT
    elif intent == "refine" and conversation_result.get("next_action") == "apply_filters":
        current_offers = context.get("available_flights", [])
        if not current_offers:
            search_params = context.get("search_params") if context else None
            if not search_params:
                search_params = load_session_state(sessionId)
            current_offers = load_offers_by_search_params(search_params)

        if current_offers:
            filters = conversation_result.get("filters", {})
            sort_by = conversation_result.get("sort_by", "price")
            sort_order = conversation_result.get("sort_order", "asc")
            result_limit = conversation_result.get("result_limit")
            
            ai_input_offers = current_offers
            if len(current_offers) > 60:
                ai_input_offers = sorted(current_offers, key=lambda x: float(str(x.get("totalPrice", 999999))))[:60]

            # Get nationality for transit rules
            nationality = conversation_result.get("passport_nationality")
            if not nationality and context:
                nationality = context.get("search_params", {}).get("passport_nationality")
            if not nationality:
                sp = load_session_state(sessionId)
                if sp: nationality = sp.get("passport_nationality")
            
            visa_confirmed = conversation_result.get("visa_confirmed", False)
            if not visa_confirmed:
                transit_rules_for_llm, restricted_countries_list = get_transit_rules_for_offers(ai_input_offers, nationality)
            else:
                transit_rules_for_llm = {}
                restricted_countries_list = []
                
            conversation_result["restricted_countries"] = restricted_countries_list

            # AI selection
            filtered_offers = ai_select_flights(
                ai_input_offers,
                user_message,
                filters=filters,
                sort_by=sort_by,
                sort_order=sort_order,
                result_limit=result_limit,
                transit_rules=transit_rules_for_llm
            )
            
            conversation_result["offers"] = filtered_offers
            conversation_result["total_offers"] = len(filtered_offers)
            conversation_result["applied_filters"] = filters

            search_params = context.get("search_params") or load_session_state(sessionId)
            if search_params:
                conversation_result["search_params"] = search_params

            # Enhanced Search Intelligence for Narration
            insights = {
                "cheapest": {},
                "fastest": {},
                "stops_count": {"direct": 0, "1-stop": 0, "2-stop": 0, "3+": 0}
            }
            
            if filtered_offers:
                # Cheapest
                cheapest = min(filtered_offers, key=lambda x: float(x.get("totalPrice", 999999)))
                insights["cheapest"] = {
                    "airline": cheapest.get("airline"),
                    "price": float(cheapest.get("totalPrice")),
                    "duration": cheapest.get("duration")
                }
                
                # Fastest
                fastest = min(filtered_offers, key=lambda x: x.get("durationMinutes", 9999))
                insights["fastest"] = {
                    "airline": fastest.get("airline"),
                    "duration": fastest.get("duration")
                }
                
                # Stops Summary
                for off in filtered_offers:
                    s = off.get("stops", 0)
                    if s == 0: insights["stops_count"]["direct"] += 1
                    elif s == 1: insights["stops_count"]["1-stop"] += 1
                    elif s == 2: insights["stops_count"]["2-stop"] += 1
                    else: insights["stops_count"]["3+"] += 1

            # Generate response
            if filtered_offers:
                narrative_payload = {
                    "type": "refine_results",
                    "count": len(filtered_offers),
                    "filters": filters,
                    "insights": insights
                }
            else:
                narrative_payload = {
                    "type": "no_results",
                    "filters": filters
                }

            conversation_result["message"] = generate_assistant_message(narrative_payload)
    
    # TRANSIT CHECK INTENT
    elif intent == "transit_check":
        nationality = conversation_result.get("passport_nationality")
        country = conversation_result.get("layover_country")
        
        if nationality and country:
            rules = check_layover_permission(country, nationality)
            if rules:
                is_allowed = rules.get("allowed", False)
                narrative_payload = {
                    "type": "transit_allowed" if is_allowed else "transit_restricted",
                    "layover_country": country,
                    "nationality": nationality,
                    "notes": rules.get("notes", ""),
                    "message": rules.get("message", "")
                }
                conversation_result["transit_rules"] = rules
                conversation_result["message"] = generate_assistant_message(narrative_payload)
            else:
                conversation_result["message"] = "I'm having trouble connecting to the transit database. Please try again."
        else:
            conversation_result["message"] = "I need both the layover country and your nationality to check transit safety."
    
    # Save assistant response
    if conversation_result.get("message"):
        save_chat(sessionId, "assistant", conversation_result["message"])

    return {
        "statusCode": 200,
        "headers": headers,
        "body": json.dumps(conversation_result, cls=DecimalEncoder)
    }

