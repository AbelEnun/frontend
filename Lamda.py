def build_destinations_for_frontend(alternative_destinations):
    """
    Enriches LLM alternative_destinations into the structured format
    the frontend DestinationCard component expects.
    """
    if not alternative_destinations or not isinstance(alternative_destinations, list):
        return []
    result = []
    seen = set()
    for alt in alternative_destinations:
        if not isinstance(alt, dict):
            continue
        iata = (alt.get("iata") or "").upper()
        city = alt.get("city") or alt.get("name") or ""
        if not city or iata in seen:
            continue
        seen.add(iata)
        info = DESTINATION_INFO.get(iata, {})
        price_from = safe_p(alt.get("price_from") or 0, default=350)
        price_to   = round(price_from * 1.35)
        card = {
            "iata":        iata,
            "city":        info.get("city") or city,
            "country":     alt.get("country") or info.get("country", ""),
            "priceRange":  f"{int(price_from)}-{int(price_to)}",
            "description": alt.get("description") or info.get("description") or f"Explore the best of {city}.",
            "bestSeason":  alt.get("bestSeason") or info.get("best_time") or "",
            "visaInfo":    alt.get("visaInfo") or info.get("visa") or "",
        }
        result.append(card)
    return result[:4]

